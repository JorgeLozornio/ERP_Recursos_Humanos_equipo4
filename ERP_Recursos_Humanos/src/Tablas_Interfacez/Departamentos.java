    private void lblBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBuscarMouseClicked
        // TODO add your handling code here:
        String sel;
        sel=(String)cbBuscar.getSelectedItem();
        
        if (sel.equals("Id")) {
            sel="IdDepartamento";
        }
        if (sel.equals("Estatus")) {
            
        
        if(txfBuscar.getText().equals("A") || txfBuscar.getText().equals("I")){
            tblDepartamentos.setModel(dep.busqueda(txfBuscar.getText(), sel ));
       txfBuscar.setText("");
        cbBuscar.setSelectedIndex(0);
        }
       else {
            JOptionPane.showMessageDialog(null, "Ingresa un valor valido (A o I)");
        }
        } else if(sel.equals("Id") || sel.equals("Nombre")){
            tblDepartamentos.setModel(dep.busqueda(txfBuscar.getText(), sel ));
            txfBuscar.setText("");
        cbBuscar.setSelectedIndex(0);
        }else if(sel.equals("Seleccione...")){
             JOptionPane.showMessageDialog(null, "Selecciona una opcion a buscar");
        }
    }//GEN-LAST:event_lblBuscarMouseClicked

