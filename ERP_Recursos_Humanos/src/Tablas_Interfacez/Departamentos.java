    private void borraRegistosTabla(JTable tabla){
        DefaultTableModel tablaTemp=(DefaultTableModel) tabla.getModel();
                
        for (int r=tabla.getRowCount()-1;r>-1;r--){
            tablaTemp.removeRow(r);
        }
        
    }
